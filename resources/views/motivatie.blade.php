        <h6>Carrièreankers</h6>
        <p>Op basis van je antwoorden is bepaald in hoeverre elke van de negen carrièreankers aansluit
            bij je persoonlijkheid. De mate waarin is weergegeven met een aantal punten.<br><br>

            De negen drijfveren zijn "rijkdom", "normen en waarden", "creativiteit",
            "onafhankelijkheid", "zekerheid", "macht", "ergens goed in zijn", "sociale contacten" en
            "status"<br><br>

            In totaal heb je 100 punten verdeeld. Een carrièreanker kan maximaal 25 punten hebben.
            Hieronder staan eerst de twee carrièreankers die je de meeste punten gaf en daaronder de
            carrièreanker die je het minst aantal punten gaf.<br><br>

            <strong>De meeste punten gaf je aan de rol: Zekerheid (21 punten)</strong>
            Voor deze mensen is zekerheid op lange termijn belangrijk. Baan en werk zijn vooral een
            middel om pensioen en hypotheek veilig te stellen. De hoogte van het inkomen is dan ook
            minder belangrijk dat de zekerheid van inkomen en het kunnen behouden van een baan. Bij
            de keuze voor een werkgever kijkt men dan ook vaak naar de stabiliteit van de
            organisatie. Het reduceren van financiële onzekerheid is een belangrijke
            drijfveer.<br><br>
            <strong>dit klopt, ik heb liever een baan die zeker is dan een baan die iets meer
                betaald.</strong><br><br>

            <strong>Daarna gaf je de meeste punten aan: Onafhankelijkheid (18 punten)</strong>
            Autonomie is een belangrijke drijfveer. Het gaat mensen met dit carrièreanker om
            onafhankelijkheid en zelf beslissingen kunnen nemen. Het is dan ook niet verwonderlijk
            dat deze mensen vaak eigen baas zijn of dat graag willen worden. In hun loopbaankeuze
            laten zij zich dan ook leiden door de vraag in welke mate zij hun eigen gang kunnen gaan
            of zelfstandig kunnen werken.<br><br>
            <strong>dit klopt wel en niet omdat ik graag onafhankelijk wil werken maar uitendelijk
                wel voor iemand anders zou willen werken omdat je voor een eigen bedrijf
                belangerijke beslissingen moet maken. hier ben ik als persoon niet goed
                in.</strong><br><br>

            <strong>Het minst aantal punten gaf je voor: Sociale contacten (0 punten)</strong>
            Mensen met deze drijfveer hechten veel waarde aan sociale contacten. Gezelligheid en
            vriendschap zijn belangrijke redenen voor het hebben van een baan. De scheiding tussen
            werk en privé geldt voor hun minder strikt. Bij loopbaankeuzes zal men zich dan ook snel
            laten leiden door de vraag of men wel voldoende collega's ontmoet of heeft. Of het werk
            bevalt hangt af van de mate waarin men kan samenwerken of onder de mensen is.<br><br>
            <strong>voor mij maakt het nier uit of ik moet samenwerken of niet. het enigste voordeel
                van samenwerken is dat je elkaar kan ondersteunen en kan motiveren.</strong>

