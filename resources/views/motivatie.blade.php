        <h1>Theorie</h1>
        <p>Motivatie is datgene wat een individu tot bepaald gedrag drijft. Motivatie heeft invloed op
            de initiatie, richting, intensiteit en volharding van het menselijk gedrag. Motivatie
            ontstaat uit een samenspel tussen de biologische (aangeboren) en cultuurafhankelijk
            (aangeleerde) eigenschappen van een individu en de omgeving van dat individu. <a
                href="#bronnenlijst" class='bronlink'>[2]</a><br><br>

            Er zijn twee verschillende soorten motivatie, Intrinsieke en extrinsieke motivatie.
            volgens Patrick Schriel is dat intinsieke motivatie motivatie is die uit eigen wil
            ontstaat en bij extrensieke motivatie moet je iets verplicht doen. <a
                href='#bronnenlijst' class='bronlink'>[3 </a></p><br>

        <h2>TED Global 2009 Dan Pink</h2>
        <p>Dan pink geeft als voorbeeld dat er verschillende motivaties bestaan door middel van een
            experiment te vertellen, het experiment was dat er twee groepen zijn die een puzzel
            getimed moeten uitvoeren. De ene groep krijgt een beloning als je sneller bent dan de
            andere mensen in de groep en de andere groep werd verteld dat hun tijd werd opgenomen,
            je zou zeggen dat de groep die gemotiveerd word door het geld het beter doet, dit
            gebeurde dus niet, de groep die een beloning kreeg deed er over het algemeen drie en een
            halve minuut langer over. Met een paar taken werkt het belonen van werk maar bijna
            altijd is dit niet van toepassing.<br><br>

            extrinsieke motivatie door middel van een beloning werkt het beste als de taak die
            uitgevoerd moet worden simpel op te lossen is en/of weinig creativiteit voor nodig hebt
            dus fysieke taken . Dit komt omdat je door de beloning tunnelvisie krijgt.<br><br>

            Daar in tegen werkt intrinsieke motivatie het beste als de taak creativiteit of
            breinkracht nodig heeft. Dan Pink zegt dat er een manier is om mensen het beste te
            motiveren. Deze methode bestaat uit 3 delen, autonomie, meesterschap en een doel. De
            methode geeft werknemers veel vrijheid over hun eigen tijd en wanneer ze aan projecten
            werken. Uiteindelijk stimuleert dit de motivatie op een intrinsieke manier. <a
                href='#bronnenlijst' class='bronlink'>[4]</a></p><br>
    </div>

